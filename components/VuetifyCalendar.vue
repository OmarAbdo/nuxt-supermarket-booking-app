<template>
  <v-row>
    <v-col>
      <v-sheet height="400">
        <v-calendar
          ref="calendar"
          :now="today"
          :value="today"
          :events="events"
          color="primary"
          type="week"
        ></v-calendar>
      </v-sheet>
    </v-col>
  </v-row>
</template>

<script lang="ts">
import Vue from 'vue'

interface calendar {
    scrollToTime: () => void
}

export default Vue

.extend({
  data: () => ({
    today: '2019-01-08',
    events: [],
  }),
  
  mounted() : void {
    const calendar: any = this.$refs.calendar;
    calendar.scrollToTime('08:00');
    this.events = this.$store.getters['calenderDatesGetter']
  },
});
</script>

